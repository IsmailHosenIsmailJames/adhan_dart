/// Enum for each prayer
/// * five obligatory prayers
///   - fajr
///   - dhuhr
///   - asr
///   - maghrib
///   - isha
/// * three additional values
///   - sunrise
enum Prayer {
  fajr,
  sunrise,
  dhuha,
  noon,
  dhuhr,
  asr,
  sunset,
  maghrib,
  isha,
  tahajjud,
  ishaBefore,
  fajrAfter,
}
